import QuantConnect.Orders
import QuantConnect.Util


class OrderEventJsonConverter(QuantConnect.Util.TypeChangeJsonConverter[QuantConnect.Orders.OrderEvent, SerializedOrderEvent]):
    """
    Defines how OrderEvents should be serialized to json
    """


class SerializedOrderEvent:
    """
    Data transfer object used for serializing an OrderEvent that was just generated by an algorithm
    """


class SerializedOrderJsonConverter(QuantConnect.Util.TypeChangeJsonConverter[QuantConnect.Orders.Order, SerializedOrder]):
    """
    Defines how Orders should be serialized to json
    """


class SerializedOrder:
    """
    Data transfer object used for serializing an Order that was just generated by an algorithm
    """


